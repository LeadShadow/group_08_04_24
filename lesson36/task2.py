# 2. Цикли і умови
# Задача: Напишіть програму, яка приймає від користувача число n. Якщо
# n > 0 виведіть усі числа від 1 до n, використовуючи цикл for. Якщо
# n ≤ 0 виведіть повідомлення: "Невірне число".

n = int(input('Введіть число n: '))

if n > 0:
    for i in range(1, n + 1):
        print(i)
else:
    print('Неправильне число')
