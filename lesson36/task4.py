# 4. Колекції (списки, словники, множини, кортежі, рядки)
# Задача: Напишіть програму, яка:
#
# Приймає список чисел від користувача.
# Повертає словник, де ключами є унікальні числа зі списку, а значеннями – їх кількість входжень.

numbers = list(map(int, input('Введіть числа через пробіл: ').split()))

counts = {}
for num in numbers:
    counts[num] = counts.get(num, 0) + 1


print('Словник кількостей: ', counts)
