# 1. Назва проекту:
# "Move and Collect"
#
# 2. Мета гри:
# Гравець керує квадратом (персонажем) на екрані, переміщується у межах вікна
# і збирає зірки для збільшення рахунку. Гра нескінченна, поки користувач її не закриє.
#
# 3. Функціональні вимоги
# 3.1 Графічний інтерфейс:
# Розмір вікна:
#
# Ширина: 400 пікселів.
# Висота: 300 пікселів.
# Фіксований розмір вікна (без можливості змінювати).
# Елементи гри:
#
# Гравець: квадрат синього кольору, розміром 30x30 пікселів.
# Зірка: коло жовтого кольору, розміром 20x20 пікселів.
# Фон: білий.
# Рахунок: текст у верхньому лівому куті, колір тексту — синій.
# Відображення екрана:
#
# Елементи гри повинні постійно оновлюватися.
# Гравець та зірка повинні бути чітко видимі на фоні.
# 3.2 Логіка гри:
# Рух гравця:
#
# Гравець рухається за допомогою клавіш:
# ↑ (вгору), ↓ (вниз), ← (ліворуч), → (праворуч).
# Рух обмежений межами вікна (гравець не може вийти за межі).
# Генерація зірки:
#
# Зірка з’являється у випадковій позиції в межах екрану. Координати повинні
# враховувати розмір зірки, щоб вона не виходила за межі вікна.
# Збір зірки:
#
# Коли гравець торкається зірки, рахунок збільшується на 1.
# Після збору, зірка повинна переміщуватися у випадкову позицію.
# Рахунок:
#
# Підрахунок зібраних зірок.
# Відображення рахунку у верхньому лівому куті вікна.
# 4. Нефункціональні вимоги
# Продуктивність:
#
# Гра повинна працювати з частотою оновлення 30 FPS (кадрів на секунду).
# Зручність використання:
#
# Інтуїтивне керування за допомогою клавіатури.
# Простий і зрозумілий інтерфейс.
# Кросплатформеність:
#
# Гра повинна запускатися на будь-якому ПК, який підтримує Python 3.8+ і бібліотеку Pygame.
